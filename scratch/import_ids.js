async function run() {
  console.log(`🚀 Iniciando importação de ${ids.length} fotos...`);
  
  let count = 0;
  for (const id of ids) {
    try {
      const res = await fetch('http://localhost:3000/api/artifacts', {
        method: 'POST',
        headers: { 'Content-Type': 'application/json' },
        body: JSON.stringify({
          type: 'PHOTO',
          title: `Foto do Acervo - ${id.substring(0, 6)}`,
          category: 'Família e Relações',
          year: 'S/D',
          mediaUrl: `https://drive.google.com/file/d/${id}/view`,
          contextDescription: 'Registro importado automaticamente via script de lote.'
        })
      });
      
      if (res.ok) {
        count++;
        if (count % 10 === 0) console.log(`✅ ${count}/${ids.length} processados...`);
      } else {
        console.error(`❌ Erro ao importar ID ${id}:`, await res.text());
      }
    } catch (e) {
      console.error(`❌ Falha de rede para ID ${id}:`, e.message);
    }
  }
  
  console.log(`\n🎉 FIM! ${count} fotos importadas com sucesso.`);
}

run();
